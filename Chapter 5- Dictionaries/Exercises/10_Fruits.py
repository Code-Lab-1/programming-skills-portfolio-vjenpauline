# Chapter 5, Exercise 10: Fruits

fruits = {
    'Strawberry':'Red',
    'Banana':'Yellow',
    'Kiwi':'Brown',
    'Pitaya':'Magenta',
    'Watermelon':'Red'
}

# Changes the colors(value) of every fruit(key)
print("These fruits can have different colors!")
fruits['Strawberry'] = 'White'
fruits['Banana'] = 'Green'
fruits['Kiwi'] = 'Green'
fruits['Pitaya'] = 'White'
fruits['Watermelon'] = 'Green'

print(fruits)